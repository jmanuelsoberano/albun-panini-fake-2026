interface FixtureSeed {
  readonly id: string;
  readonly groupId: string;
  readonly homeCode: string;
  readonly awayCode: string;
  readonly venue: string;
  readonly kickoffLabel: string;
  readonly homeScore?: number;
  readonly awayScore?: number;
  readonly events?: readonly GoalSeed[];
}

interface GoalSeed {
  readonly teamCode: string;
  readonly playerName: string;
  readonly minute?: number | null;
}

function goal(teamCode: string, playerName: string, minute: number | null = null): GoalSeed {
  return { teamCode, playerName, minute };
}

function goalEvents(matchId: string, goals: readonly GoalSeed[] = []): readonly MatchEvent[] {
  return goals.map((item, index) => ({
    id: `${matchId}-G${index + 1}`,
    minute: item.minute ?? null,
    teamId: teamId(item.teamCode),
    playerName: item.playerName,
    type: 'goal',
  }));
}

function fixture(seed: FixtureSeed): TournamentMatch {
  const finished = seed.homeScore !== undefined && seed.awayScore !== undefined;

  return {
    id: seed.id,
    phase: 'group',
    groupId: seed.groupId,
    homeTeamId: teamId(seed.homeCode),
    awayTeamId: teamId(seed.awayCode),
    homeScore: seed.homeScore ?? null,
    awayScore: seed.awayScore ?? null,
    status: finished ? 'finished' : 'scheduled',
    venue: seed.venue,
    kickoffLabel: seed.kickoffLabel,
    events: goalEvents(seed.id, seed.events),
  };
}

