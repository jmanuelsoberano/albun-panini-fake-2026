const teamIdByCode = new Map<string, string>(tournamentTeams.map((team) => [team.code, team.id]));

function teamId(code: string): string {
  const id = teamIdByCode.get(code);

  if (!id) {
    throw new Error(`Equipo no encontrado para el codigo ${code}`);
  }

  return id;
}

